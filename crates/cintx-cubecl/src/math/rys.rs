/// Clenshaw backward recurrence for a 14-coefficient Chebyshev polynomial.
///
/// Evaluates the polynomial for the given normalized argument `u` using the
/// Clenshaw algorithm. Takes 14 Chebyshev coefficients c0..c13 individually
/// (CubeCL `#[cube]` functions cannot index const arrays by runtime index).
///
/// Source: polyfits.c `_CINT_clenshaw_d1`, adapted to single-root scalar form.
#[cube]
pub fn clenshaw_d1(
    c0: f64, c1: f64, c2: f64, c3: f64, c4: f64, c5: f64,
    c6: f64, c7: f64, c8: f64, c9: f64, c10: f64, c11: f64,
    c12: f64, c13: f64, u: f64,
) -> f64 {
    let u2 = 2.0_f64 * u;
    let mut g = c13;
    let mut d = 0.0_f64;
    let tmp = u2 * g - d + c12; d = g; g = tmp;
    let tmp = u2 * g - d + c11; d = g; g = tmp;
    let tmp = u2 * g - d + c10; d = g; g = tmp;
    let tmp = u2 * g - d + c9;  d = g; g = tmp;
    let tmp = u2 * g - d + c8;  d = g; g = tmp;
    let tmp = u2 * g - d + c7;  d = g; g = tmp;
    let tmp = u2 * g - d + c6;  d = g; g = tmp;
    let tmp = u2 * g - d + c5;  d = g; g = tmp;
    let tmp = u2 * g - d + c4;  d = g; g = tmp;
    let tmp = u2 * g - d + c3;  d = g; g = tmp;
    let tmp = u2 * g - d + c2;  d = g; g = tmp;
    let tmp = u2 * g - d + c1;  d = g; g = tmp;
    u * g - d + 0.5_f64 * c0
}

